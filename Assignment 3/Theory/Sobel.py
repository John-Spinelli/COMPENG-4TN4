def sobel(G, len):
    for row in range(1,len):
        print('0\\angle0&')
        for col in range(1,len):
            dx = 0
            dy = 0
            # Constant Col
            dx += G[row-1][col+1] - G[row-1][col-1]
            dx += (G[row][col+1] - G[row][col-1])*2
            dx += G[row+1][col+1] - G[row+1][col-1]
            # Constant Row
            dy += G[row-1][col-1] - G[row+1][col-1]
            dy += (G[row-1][col] - G[row+1][col])*2
            dy += G[row-1][col+1] - G[row+1][col+1]
                    
            #dx = G[row][col+1] - G[row][col-1]
            #dy = - G[row+1][col] + G[row-1][col]
            magnitude[row][col] = round(math.sqrt(dx**2+dy**2),1)        
            angle[row][col] = round(math.atan2(dy,dx)*180/math.pi)
            
            print(str(magnitude[row][col])+'\\angle'+str( angle[row][col])+'&')
            
        
        print('0\\angle0\\\\')    
        print()


